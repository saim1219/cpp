#include<iostream>
using namespace std;
int main()
{
    int s;
    long long l;
    float f;
    double d;
    char c;
    cout<<"Size of int datatype    : "<<sizeof(s)<<" bytes.\n";
    cout<<"Size of long datatype   : "<<sizeof(l)<<" bytes.\n";
    cout<<"Size of float datatype  : "<<sizeof(f)<<" bytes.\n";
    cout<<"Size of double datatype : "<<sizeof(d)<<" bytes.\n";
    cout<<"Size of char datatype   : "<<sizeof(c)<<" bytes.\n";
    return 0;
}